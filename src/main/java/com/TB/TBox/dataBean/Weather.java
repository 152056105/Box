package com.TB.TBox.dataBean;

public class Weather {
private int localWeatherId;
private String area;
private String localTime;
private String weather;
private String temperature;
private String activity;
private String wearCloath;
private int sevenOrRain;
public int getLocalWeatherId() {
	return localWeatherId;
}
public void setLocalWeatherId(int localWeatherId) {
	this.localWeatherId = localWeatherId;
}
public String getArea() {
	return area;
}
public void setArea(String area) {
	this.area = area;
}
public String getLocalTime() {
	return localTime;
}
public void setLocalTime(String localTime) {
	this.localTime = localTime;
}
public String getWeather() {
	return weather;
}
public void setWeather(String weather) {
	this.weather = weather;
}
public String getTemperature() {
	return temperature;
}
public void setTemperature(String temperature) {
	this.temperature = temperature;
}
public String getActivity() {
	return activity;
}
public void setActivity(String activity) {
	this.activity = activity;
}
public String getWearCloath() {
	return wearCloath;
}
public void setWearCloath(String wearCloath) {
	this.wearCloath = wearCloath;
}
public int getSevenOrRain() {
	return sevenOrRain;
}
public void setSevenOrRain(int sevenOrRain) {
	this.sevenOrRain = sevenOrRain;
}


}
